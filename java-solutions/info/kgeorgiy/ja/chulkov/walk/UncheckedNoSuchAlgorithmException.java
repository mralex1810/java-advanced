package info.kgeorgiy.ja.chulkov.walk;

public class UncheckedNoSuchAlgorithmException extends RuntimeException {
    public UncheckedNoSuchAlgorithmException() {
    }

    public UncheckedNoSuchAlgorithmException(String message) {
        super(message);
    }

    public UncheckedNoSuchAlgorithmException(String message, Throwable cause) {
        super(message, cause);
    }

    public UncheckedNoSuchAlgorithmException(Throwable cause) {
        super(cause);
    }

    public UncheckedNoSuchAlgorithmException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}
